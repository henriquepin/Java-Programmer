package cap4;

public class ExemploIfElse {
	public static void main(String[] args) {
		int x = 16;
		
		if(x < 15){
			System.out.println("x é menor que 15");
			System.out.println("o valor de x é " + x);
		}
		else{
			System.out.println("x é maior ou igual a 15");
			System.out.println("o valor de x é " + x);
		}
	}
}
